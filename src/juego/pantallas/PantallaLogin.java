    @Override
    public void OnLoginOK(EventoMensaje eventoMensaje) {
        String resString = ClienteUtils.LeerOK(eventoMensaje.getMensaje().getMensajeServidor());
        int res = Integer.parseInt(resString);
        switch (res) {
            case 0:
                //no se pudo loguear, no coinciden ambos datos
                txt_userlogin.setText("");
                txt_passwordlogin.setText("");
                JOptionPane.showMessageDialog(rootPane, "Usuario no existe !!");
                break;
            case 1:
                //logueado exitosamente
                Cliente cli = clienteSocket.getEventoClienteS().getCliente();
                cli.setLogin(true);
                cli.setUser(txt_userlogin.getText());
                cli.setPassword(new String(txt_passwordlogin.getPassword()));
                cli.setId(Long.parseLong(ClienteUtils.LeerOKnewID(eventoMensaje.getMensaje().getMensajeServidor())));
                
                lbl_idcliente.setText(String.valueOf( "ID asignado = " + cli.getId()));
                btn_login.setEnabled(false);
                btn_registrar.setEnabled(false);
                btn_logout.setVisible(true);
                JOptionPane.showMessageDialog(rootPane, "Logueado Exitosamente !!");
                
                this.setVisible(false);
                Juego.pantallaJuego.setVisible(true);
                
                clienteSocket.ComenzarPartida();
                break;
            case 2:
                //contrasenha incorrecta
                JOptionPane.showMessageDialog(rootPane, "Contrasenha Incorrecta !!");
            default:
                System.out.println("Ocurrio un error al loguearse!!");
        }
    }

    @Override
    public void OnRegistroOK(EventoMensaje eventoMensaje) {
        int res = Integer.parseInt(eventoMensaje.getMensaje().getMensajeServidor());
        switch (res) {
            case 0:
                //no se pudo registrar
                //clienteSocket.getEventoClienteS().getCliente().Limpiar();
                JOptionPane.showMessageDialog(rootPane, "No se pudo registrar. Nombre de usuario ya existe !!");
                break;
            case 1:
                //registrado exitosamente
                txt_userreg.setText("");
                txt_passwordreg1.setText("");
                txt_passwordreg2.setText("");
                
                JOptionPane.showMessageDialog(rootPane, "Registrado Exitosamente !!");
                
                break;
            default:
                System.out.println("Ocurrio un error al Registrarse!!");
        }
    }

