    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        // TODO add your handling code here:
        String user = txt_userlogin.getText();
        String password = new String(txt_passwordlogin.getPassword());
        if (!user.isEmpty() && !password.isEmpty()) {
            clienteSocket.Loguear(user, password);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Campos llenados incorrectamente!");
        }
    }//GEN-LAST:event_btn_loginActionPerformed

    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
        // TODO add your handling code here:
        String user = txt_userreg.getText();
        String password1 = new String(txt_passwordreg1.getPassword());
        String password2 = new String(txt_passwordreg2.getPassword());
        if (!user.isEmpty() && !password1.isEmpty() && !password2.isEmpty()) {
            if (password1.equals(password2)) {
                clienteSocket.Registrar(user, password1);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Los passwords no coinciden!!");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Campos llenados incorrectamente!");
        }
    }//GEN-LAST:event_btn_registrarActionPerformed

